print("Descubra o resultado e o resto da divisão")

dividendo = int(input("Insira o dividendo valor"))
divisor = int(input("Insira o divisor valor"))

resultado = dividendo / divisor
resto = dividendo % divisor

print(f"O dividendo é {dividendo}\nO divisor é {divisor}\nO resultado é {resultado}\nO resto é {resto}")